package handler

import (
	"log"
	"net/http"

	"github.com/gin-gonic/gin"
	"github.com/ngoctb13/forya-be/handler/models/request"
)

func (h *Handler) CreateSupplyBatch() gin.HandlerFunc {
	return func(c *gin.Context) {
		req := &request.CreateSupplyBatchRequest{}
		if err := c.ShouldBind(req); err != nil {
			log.Printf("parse request error: %v", err)
			c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
			return
		}

		input, err := req.ValidateAndMap()
		if err != nil {
			log.Printf("validate request error: %v", err)
			c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
			return
		}

		if err := h.supplyBatch.CreateSupplyBatch(c, input); err != nil {
			log.Printf("CreateSupplyBatchUsecase fail with error: %v", err)
			c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
			return
		}

		c.JSON(http.StatusOK, gin.H{"message": "create supply batch successfully"})
	}
}
